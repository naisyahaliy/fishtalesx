            <h4>Jenis-jenis Lumba-lumba</h4>
            <h5><b>1.Lumba-lumba Hidung Botol</b></h5>
            <img src="https://cdn.idntimes.com/content-images/community/2022/09/1024px-bottlenose-dolphin-1498971217-726107e6cb24f94eb96bf875ffbf43f3-62f9a53e9dd8470c7bb67ede32257f8f_600x400.jpg" >
            <p style="margin-top: 2%;">Lumba-lumba hidung botol adalah predator laut teratas yang berumur panjang, memakan berbagai jenis ikan, serta cumi-cumi dan gurita. Lumba-lumba mencapai kematangan pada usia sekitar 6 tahun dan dapat hidup selama 40-50 tahun. Beberapa lumba-lumba hidung botol di pantai timur Skotlandia telah terlihat dalam populasi tersebut selama lebih dari 30 tahun. Mereka juga merupakan hewan sosial, biasanya hidup berkelompok dan terkadang dilaporkan dalam kelompok spesies campuran, termasuk lumba-lumba Risso di pantai barat.</p>
            <p><b>Kenapa sih di sebut lumba-lumba hidung botol?</b></p>
            <p>Lumba-lumba hidung botol disebut demikian karena memiliki moncong yang pendek dan tebal, yang dalam bahasa Latin disebut rostrum. Moncong ini terbentuk dari rahang atas dan bawah lumba-lumba yang memanjang. </p>

            <h5><b>2.Lumba-lumba sungai amazon</b></h5>
            <p><b>Kenapa ya di sebut lumba-lumba sungai amazon?</b></p>
            <p>Lumba-lumba sungai Amazon disebut demikian karena habitatnya berada di sungai Amazon dan Orinoco. Lumba-lumba sungai Amazon juga dikenal dengan nama lumba-lumba merah muda.</p>
            <img src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcQ3ZMz64dNz30A2lpFZPJk76vU2b2wFtTrEqLmf3GejC-PlYgzV" width="40%;" >
            <p style="margin-top: 2%;">Lumba-lumba sungai Amazon adalah spesies air tawar unik yang hanya ditemukan di sungai-sungai Amerika Selatan. Warna merah mudanya yang mencolok dan lehernya yang fleksibel, yang memungkinkannya untuk berputar dan berputar melalui hutan yang tergenang air selama musim air tinggi, menjadikannya spesies lumba-lumba yang paling tidak biasa dan unik, dan salah satu yang memainkan peran penting dalam kepercayaan budaya dan cerita rakyat di seluruh wilayahnya.</p>

            <h5><b>3.Lumba-lumba bungkuk punggung</b></h5>
            <p><b>Makin kesini namanya makin aneh yaaa, yu simak penjelasannya!</b></p>
            <p>Lumba-lumba bungkuk adalah anggota genus Sousa . Lumba-lumba ini dicirikan oleh punuk yang mencolok dan sirip punggung memanjang yang ditemukan di punggung spesies dewasa. Lumba-lumba bungkuk mendiami perairan dekat pantai yang dangkal di sepanjang garis pantai di Australia , Afrika , dan Asia . Kegemaran mereka terhadap habitat ini membuat mereka terpapar pada berbagai aktivitas manusia seperti terjerat dalam penangkapan ikan, lalu lintas perahu, polusi, dan hilangnya habitat. Meskipun ada risiko ini, keberadaan mereka di dekat pantai memudahkan pengamatan dari daratan.</p>
            <img src="https://encrypted-tbn2.gstatic.com/images?q=tbn:ANd9GcRFqNNwSrZbQ2_8_rXjJVYGTg7FpJSjobGOxUM0pPYl8G4HNNw3">
            <p style="margin-top: 2%;">Lumba-lumba ini dicirikan oleh punuk yang mencolok dan sirip punggung yang memanjang yang ditemukan di punggung spesies dewasa.</p>

            <h5><b>4.Lumba-lumba totol pantropis</b></h5>
            <img src="https://inaturalist-open-data.s3.amazonaws.com/photos/71204651/large.jpg" >
            <p style="margin-top: 2%;"><b>Lumba-lumba totol</b>,atau dikenal dengan nama lumba-lumba berbintik (Stenella attenuata), adalah spesies lumba-lumba yang terkenal karena pola bintik-bintik di tubuhnya. Lumba-lumba ini sering ditemukan di perairan hangat dan tropis di seluruh dunia, termasuk Samudra Atlantik, Pasifik, dan Hindia.</p>
            <p><b>Alasan Disebut Lumba-Lumba Totol:</b></p>
            <p>Lumba-lumba ini disebut "totol" atau "berbintik" karena pola bintik-bintik yang berkembang di tubuh mereka seiring bertambahnya usia. Ketika lahir, tubuh mereka berwarna abu-abu polos, tetapi seiring waktu, bintik-bintik hitam dan putih mulai muncul di seluruh tubuh mereka, terutama di bagian punggung dan perut. Semakin tua lumba-lumba, semakin banyak bintik-bintik yang mereka miliki.</p>

            <h5><b>5.Lumba-lumba pemintal</b></h5>
            <img src="https://encrypted-tbn1.gstatic.com/images?q=tbn:ANd9GcTguKxTOjdLTWUzn0Yu-3gjb0vb494eRxFH2IsARG0W6BxGEX_r" >
            <p style="margin-top: 2%;"><b>Lumba-lumba pemintal</b>atau <b>spinner dolphin (Stenella longirostris)</b> adalah spesies lumba-lumba yang terkenal karena perilaku melompat dan berputarnya yang luar biasa. Mereka disebut "pemintal" karena kemampuan unik mereka untuk melakukan putaran berulang-ulang di udara ketika melompat keluar dari air. Ini adalah salah satu ciri yang paling menarik dari spesies ini, dan menjadi alasan utama mengapa mereka disebut lumba-lumba pemintal.</p>
            <p><b>Alasan Disebut Lumba-Lumba Pemintal:</b></p>
            <p>Lumba-lumba pemintal mendapatkan namanya dari kebiasaan mereka yang sangat khas, yaitu <b>melakukan putaran cepat di udara</b> saat melompat dari air. Mereka bisa berputar di sekitar poros tubuh mereka beberapa kali dalam satu lompatan. Putaran ini dapat mencapai empat hingga tujuh kali sebelum kembali ke air.</p>

