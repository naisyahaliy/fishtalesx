            <p>Meski cukup terkenal, fakta-fakta tentang gurita masih jarang diketahui secara luas.Salah satunya adalah fakta bahwa gurita memiliki tiga jantung. Ingin mengetahui lebih lanjut mengenai gurita? Simak fakta-faktanya di bawah ini, yuk.</p>
        <p><b>1.Tubuhnya fleksibel</b></p>
        <p>Karena bertubuh lunak, gurita memiliki tubuh yang fleksibel. Hal ini memungkinkan mereka untuk melewati celah batuan yang sangat sempit di bawah laut. Kemampuan tersebut mereka gunakan untuk melarikan diri dari ikan pemangsa seperti belut laut.</p>
        <p><b>2.Memiliki tinta yang berbahaya</b></p>
        <p>Smithsonian Magazines mengungkapkan bahwa gurita memiliki tinta yang mampu membuat musuhnya terluka karena mengandung senyawa yang disebut tirosinase. Ketika disemprotkan ke mata predator, tirosinase menyebabkan iritasi yang mampu membutakan dan juga merusak indra penciuman serta rasa makhluk yang dikenainya.Senyawa yang terkandung pada tinta terlalu manjur, sehingga gurita yang tidak lepas dari tinta mereka sendiri juga bisa mati.</p>
        <p><b>3.Hewan Penyendiri</b></p>
        <p>Gurita cenderung lebih senang menyendiri, meskipun terkadang mereka juga berinteraksi dengan sesama gurita lain. Dilansir Live Science, beberapa spesies gurita berburu secara individu di malam hari, sementara yang lain hanya berburu saat senja dan subuh.</p>
