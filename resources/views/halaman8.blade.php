    <h2 style="font-family: 'Franklin Gothic Medium', 'Arial Narrow', Arial, sans-serif;">Jenis-jenis Gurita</h2><br>
    <h3>1.Coconut Octopus</h3>
    <img src="https://cdn.idntimes.com/content-images/community/2019/10/octopus-with-coconut-shell-0c67a32150bfa9cd2b21fadd10e80fa5.jpg" style="width: 50%; margin-top: 3%; margin-left:25%;"><br>
    <p style="margin-top: 3%;">Gurita kelapa atau coconut octopus merupakan jenis gurita yang terbilang paling unik. Dengan warna cokelat keunguan gurita ini kerap kali menjadikan kelapa sebagai tempat perlindungannya, dari situlah asal nama gurita ini muncul. Lucunya lagi gurita kelapa akan membawa 'cangkang' palsunya kemanapun mereka pergi dengan memegangnya menggunakan tentakel.</p>
    
    <h3>2.Mimic octopus</h3>
    <img src="https://cdn.idntimes.com/content-images/community/2019/10/1280-486022113-mimic-octopus-ea20a2bbcc37463f7efa74acb9003b8f.jpg" style="width: 50%; margin-top: 3%; margin-left:25%;margin-left:25%;"><br>
    <p style="margin-top: 3%;">Sebagai masyarakat lokal Indonesia kita perlu berbangga, pasalnya gurita cantik yang satu ini pertama kali ditemukan di wilayah Sulawesi pada tahun 1998. Sekilas mimic octopus atau gurita penyamar memiliki motif loreng kecokelatan mirip dengan lion fish, gurita ini pandai beradaptasi dengan menyesuaikan warna tubuhnya seperti lingkungan sekitar. Sengatan gurita penyamar cukup berbahaya sehingga kamu gak boleh terkecoh melihat warna indah yang bikin pengen menyentuhnya.</p>

      <h3>3.Hapalochlaena</h3>
      <img src="https://cdn.idntimes.com/content-images/community/2019/10/5c54bc9aeb3ce819e7347fc3-750-563-7aa0701ab4a1866b023aa646c4c32704.jpg" style="width: 50%; margin-top: 3%; margin-left:25%;"><br>
      <p style="margin-top: 3%;">Hapalochlaena adalah nama ilmiah dari gurita cincin biru. Gurita ini punya penampilan yang lebih cantik dari gurita lainnya. Cincin biru pada tubuhnya dapat menyala dalam kegelapan. Namun ini menjadikannya sebagai hewan yang tergolong ke dalam hewan berbisa. Gurita cincin biru dapat ditemukan di sekitar Samudra Hindia, Jepang dan Australia.</p>
      
      <h3>4.  Octopus briareus</h3>
      <img src="https://cdn.idntimes.com/content-images/community/2019/10/i0000j75rr6pg24-67182919946c61c4fdd7fd3abd082c8d.jpg" style="width: 50%; margin-top: 3%;  margin-left:25%;"><br>
      <p style="margin-top: 3%;"> Octopus briareus atau gurita karang Karibia merupakan jenis gurita dengan penampakan yang berbeda dari kebanyakan jenis lainnya. Gurita ini bewarna putih pekat dengan ukuran yang cukup besar.</p>
        <p style="margin-top: 3%;">Selain warna putih  Octopus briareus juga memiliki warna lain seperti warna bening kemerahan. Gurita ini disebut sebagai gurita karang karena suka bersembunyi dan menyesuaikan warna sesuai dengan karang.<p>

        <h3>5.Grimpoteuthis.</h3>
      <img src="https://cdn.idntimes.com/content-images/community/2019/10/vxndqdcdcfvkoqrkxfnxxm-adefe0088b290c519ceae929a15ef19a.jpg" style="width: 50%; margin-top: 3%;  margin-left:25%;"><br>
      <p style="margin-top: 3%;">Grimpoteuthis alias gurita dumbo memiliki penampilan yang berbeda dari spesies lainnya. Grimpoteuthis memiliki 'payung' yang menyerupai telinga. Nama dumbo diambil karena bentuknya menyerupai sepasang telinga gajah dalam serial animasi Dumbo. Meskipun berbentuk menggemaskan tapi sayangnya gurita jenis ini sulit ditemukan karena hidup di laut yang sangat dalam.</p>

        <p style="font-family: 'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"><b>"Dalam sunyi dan kedalaman laut, gurita menari bebas, meninggalkan jejak keajaiban alam bawah laut.Marilah kita jaga laut dan seluruh isinya. Dengan upaya pelestarian dan edukasi, kita dapat memastikan bahwa generasi mendatang akan terus terpesona oleh keindahan kuda laut dan merasakan keajaiban bawah laut."</b></p>
