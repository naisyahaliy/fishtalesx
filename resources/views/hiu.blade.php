            <p style="margin-top: 3%;">Diterjemahkan dari bahasa Inggris-Hiu lemon merupakan salah satu spesies hiu dari famili Carcharhinidae yang terkenal dengan warnanya yang kekuningan sehingga menginspirasi nama umumnya. Ia diklasifikasikan sebagai spesies Rentan oleh Persatuan Internasional untuk Konservasi Alam. Hiu lemon bisa tumbuh hingga panjang 3,4 meter.</p>
