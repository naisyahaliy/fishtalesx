            <p>Hiu adalah salah satu makhluk laut yang paling menakjubkan dan sering kali disalahpahami. Meskipun sering digambarkan sebagai predator ganas dalam film dan media, hiu sebenarnya memiliki banyak aspek menarik yang jarang diketahui. Dalam artikel ini, kita akan menjelajahi beberapa fakta menarik tentang hiu yang mungkin belum kamu ketahui. Yuk, simak dan temukan keajaiban dunia hiu!</p>

            <p><b>1. Hiu Telah Ada Sejak Jaman Purba</b> Hiu telah ada di lautan selama lebih dari 400 juta tahun, jauh sebelum dinosaurus muncul. Mereka telah melalui berbagai perubahan evolusi tetapi tetap menjadi salah satu predator puncak di lautan. Fosil hiu yang ditemukan menunjukkan bahwa mereka telah bertahan dari berbagai peristiwa kepunahan yang menghancurkan banyak spesies lainnya.</p>
            <p><b>2. Indra Hiu Sangat Tajam </b> dikenal memiliki indra yang sangat tajam, yang membantu mereka menjadi pemburu yang efektif. Mereka memiliki kemampuan penciuman yang luar biasa dan dapat mendeteksi setetes darah dalam air sejauh beberapa mil. Selain itu, hiu juga memiliki organ khusus yang disebut ampula Lorenzini, yang memungkinkan mereka mendeteksi medan listrik yang dihasilkan oleh mangsanya.</p>
            <p><b>3. Hiu Beberapa Spesies Dapat Berkembang Biak Secara Parthenogenesis</b> Parthenogenesis adalah proses reproduksi di mana betina dapat menghasilkan keturunan tanpa fertilisasi oleh jantan. Meskipun jarang, beberapa spesies hiu telah diamati berkembang biak secara parthenogenesis, terutama dalam kondisi di mana populasi jantan rendah atau tidak ada.</p>
