    <h2 style="font-family: 'Franklin Gothic Medium', 'Arial Narrow', Arial, sans-serif;">Jenis-jenis Bintang Laut</h2><br>
    <h3><b>1.Kerang Mutiara (Pearl Oyster)</b></h3>
    <img src="https://images-cdn.ubuy.co.id/633af631867a4f5e065da218-cultured-pearls-in-oysters-cultured.jpg" style="width: 50%; margin-top: 3%;"><br>
    <p style="margin-top: 3%;">Kerang mutiara atau tiram mutiara adalah kumpulan kerang yang termasuk dalam genus Pinctada. Secara taksonomi dimasukkan ke dalam hewan berjenis invertebrata, yang berarti hewan tidak bertulang belakang dan filum Mollusca yang berarti bertubuh lunak.</p>
    <h3><b>Keunikan Bulu Seribu</b></h3>
    <p><b>1.Bintang laut dengan banyak lengan</b></p>
    <p style="margin-top: 3%;">Bulu seribu merupakan bintang laut tropis yang berukuran cukup besar. Dicatat oleh laman Animal Diversity, bulu seribu dewasa memiliki total diameter antara 25—35 sentimeter. Beberapa bahkan bisa tumbuh lebih dari 70 sentimeter!Bintang laut bernama ilmiah Acanthaster planci ini punya 8—21 lengan. Sama seperti bintang laut pada umumnya, mereka bisa menumbuhkan lagi lengannya yang terputus walaupun perlu waktu setidaknya 6 bulan untuk proses regenerasinya, menurut laman Critter Facts. Warna bulu seribu juga bervariasi. Ada yang berwarna abu-abu, merah-oranye, sampai keunguan. Makanan dan tempat tinggal bulu seribu diduga memengaruhi pewarnaan tubuhnya.</p>

    <p style="margin-top: 3%;"><b>2.Tubuhnya berselimutkan duri tajam beracun!</b></p>
    <p style="margin-top: 3%;">Yang unik dari bintang laut satu ini adalah duri-duri tajam yang menyelimuti tubuhnya. Tampilannya yang bak mahkota menginspirasi penamaannya dalam bahasa Inggris, yakni crown of thorns atau mahkota berduri. Menurut Critter Facts, duri bulu seribu bisa tumbuh hingga panjang 5 sentimeter. Duri-duri ini digunakan untuk mempertahankan diri dari predator. Ya, durinya cukup tajam untuk bisa melukai tubuh predator, bahkan bisa menembus pakaian selam! Penyelam yang menyelam di area tempat tinggal bulu seribu selalu disarankan untuk berhati-hati.Racun yang terkandung dalam jaringan duri bulu seribu masih belum diketahui benar. Namun, laman Thoughtco mengungkap kalau duri bulu seribu bisa menyebabkan rasa sakit menyengat, bengkak, mual, sampai muntah-muntah pada manusia.</p>

    <p style="margin-top: 3%;"><b>3.Santap banyak karang per hari</b></p>
    <p style="margin-top: 3%;">Bulu seribu merupakan koralivor atau hewan pemakan karang. Saat makan, mereka akan menempel pada karang lalu mengeluarkan perutnya. Enzim pencernaan kemudian keluar dari perut untuk mencerna karang, lalu bulu seribu tinggal menyerap nutrisinya. Proses ini bisa memakan waktu sampai beberapa jam. Biarpun begitu, mereka menyantap banyak karang per hari, lho. Dilansir laman Animal Spot, seekor bulu seribu yang tinggal di Great Barrier Reef mengonsumsi 60—155 sentimeter persegi per harinya saat musim dingin. Angkanya meningkat sampai 478 sentimeter persegi saat musim panas. </p>

    <h3><b>2.Kerang Abalone</b></h3>
    <img src="https://akcdn.detik.net.id/visual/2019/08/22/15cf22b4-fd19-42d3-9a0b-0658358609ca_169.jpeg?w=650&q=90" style="width: 50%; margin-top: 3%;"><br>
    <p style="margin-top: 3%;">Abalon atau ngarun ialah suatu spesies kerang-kerangan dari famili Haliotidae dan genus Haliotis. Ia dikenal pula sebagai kerang mata tujuh atau siput balik batu, ormer di Jersey dan Guernsey, perlemoen di Afrika Selatan, dan pāua di Selandia Baru. Kerang mata tujuh tergolong dalam kelas Gastropoda yang besar.</p>
    <p><b>Kerang Abalone ini punya keunikan loh!  yu simak!</b></p>
     <p>-Harga mahal: Kerang abalone dikenal sebagai kerang yang mahal karena jumlahnya terbatas dan hanya bisa ditemukan di laut dalam.</p>
      <p>-Rasa manis, asin, dan mentega: Abalone memiliki rasa manis, asin, dan mentega alami.</p>
      <p>-Tekstur kenyal: Abalone memiliki tekstur yang kenyal seperti permen karet.</p>
      <p>-Kaya nutrisi: Abalone merupakan makanan laut yang kaya nutrisi seperti protein, vitamin E, magnesium, kolin, kalsium, dan fosfor. </p>
      <p><b>Kerang ini juga banyak manfaatnya loh buat kesehatan, diantaranya:</b></p>
      <p>1.Meningkatkan sistem Kekebalan tubuh</p>
      <p>2.Menurunkan resiko penyakit jantung</p>
      <p>3.Menjaga kesehatan hati</p>
      <p>4.Menjaga kesehatan tulang</p>
      <p>5.Mengelola berat badan</p>

      <h3><b>3.Kerang Scallop (Kerang Kampak)</b></h3>
      <img src="https://asset.kompas.com/crops/b9ivojAWBSanluD71hxGjM9ZYTY=/0x0:1000x667/750x500/data/photo/2020/10/20/5f8e778e43f6a.jpg" style="width: 50%; margin-top: 3%;"><br>
      <p style="margin-top: 3%;"> </p>
     <p>Kerang Scallop, atau dalam bahasa Indonesia dikenal sebagai Kerang Kampak, adalah salah satu jenis kerang laut yang memiliki bentuk cangkang menyerupai kipas.Cangkang kerang scallop berbentuk kipas dengan tepi bergerigi. Warna cangkangnya dapat bervariasi, dari putih, cokelat, hingga merah dan jingga, yang memberikan tampilan yang menarik.Kerang scallop biasanya ditemukan di dasar laut yang berpasir atau berlumpur, terutama di daerah pesisir dan lautan dangkal.</p>
     <p><b>Keunikan Kerang Scallop:</b></p>
     <p>1.Kemampuan Bergerak Cepat:
        Tidak seperti kebanyakan kerang yang menetap di satu tempat atau bergerak sangat lambat, kerang scallop memiliki kemampuan untuk "melompat" atau bergerak dengan membuka dan menutup cangkangnya dengan cepat. Gerakan ini membuatnya bisa bergerak maju untuk menghindari predator.</p>
        <p>2.Mata yang Banyak:
         Salah satu keunikan dari kerang scallop adalah ia memiliki banyak mata kecil di sepanjang tepi mantel cangkangnya. Mata ini memungkinkan scallop mendeteksi cahaya dan bayangan, membantu mereka mendeteksi predator yang mendekat.</p>
        <p>3.Daging yang Lezat:
        Kerang scallop sangat terkenal dalam dunia kuliner, terutama bagian otot aduktor (bagian yang digunakan untuk membuka dan menutup cangkang). Daging kerang scallop memiliki tekstur yang lembut dan rasa yang manis, sehingga menjadi salah satu makanan laut yang mewah di berbagai restoran.</p>
        <p>4.Kerang kampak memiliki tekstur daging yang halus.</p>
      
      <h3><b>4.Kerang kepala kambing</b></h3>
      <img src="https://gardaanimalia.com/wp-content/uploads/2022/01/kerang-kepala-kambing-atau-moluska.jpeg" style="width: 50%; margin-top: 3%;"><br>
      <p style="margin-top: 3%;">Cassis cornuta atau kerang kepala kambing adalah salah satu jenis moluska yang memiliki ukuran cangkang terbesar.Hewan bertubuh lunak dengan nama lain kerang helm ini umumnya bisa memiliki ukuran 22 sentimeter hingga dapat mencapai ukuran maksimal 36 sentimeter.</p>
      <p><b>Fakta unik kerang kepala kambing:</b></p>
      <p>Kerang kepala kambing (Cassis Cornuta) memiliki cangkang yang sangat besar, padat, berat, dan gemuk. </p>
      <p>Cangkangnya memiliki kenop besar yang menyerupai tanduk dan dasar yang lebar dan rata.</p>
      <p>Kerang kepala kambing merupakan predator bagi bintang laut mahkota duri (Acanthaster planci).</p>
      <p>Kerang kepala kambing berperan penting dalam menjaga kelestarian karang dan menyeimbangkan ekosistem di laut</p>
      <p>Kerang kepala kambing terancam punah di banyak tempat karena menjadi sasaran perburuan untuk diambil dagingnya.</p>

      <h3><b>5.Kerang Bulu</b></h3>
      <img src="https://images.tokopedia.net/img/cache/700/VqbcmM/2022/7/11/d5de9e73-1e8d-457b-8250-3176299cc9aa.jpg" style="width: 50%; margin-top: 3%;"><br>
      <p style="margin-top: 3%;">Kerang bulu atau dikenal juga dengan nama kerang bulu-bulu (Anadara antiquata) adalah sejenis kerang yang hidup di perairan dangkal dan sering ditemukan di wilayah Asia Tenggara, termasuk Indonesia dan Malaysia.<p>
        <p><b>Berikut adalah beberapa manfaat dan keunikannya:</b></p>
      <p>1.Sumber Protein yang Tinggi
        Kerang bulu merupakan sumber protein hewani yang baik untuk tubuh. Protein ini penting untuk pertumbuhan, memperbaiki jaringan tubuh, dan menjaga kesehatan otot.</p>
      <p>2.Mengandung Zat Besi
        Kerang bulu kaya akan zat besi, yang penting untuk memproduksi hemoglobin dalam darah. Ini membantu mencegah anemia dan meningkatkan aliran oksigen ke seluruh tubuh.</p>
      <p>3.Sumber Omega-3
        Seperti kebanyakan kerang laut, kerang bulu juga mengandung asam lemak omega-3 yang bermanfaat untuk menjaga kesehatan jantung, mengurangi peradangan, dan mendukung fungsi otak.</p>
        <p><b>Keunikan Kerang Bulu:</b></p>
      <p>1.Bulu-Bulu di Cangkangnya
        Sesuai namanya, kerang bulu memiliki ciri khas berupa bulu-bulu halus di cangkangnya. Bulu-bulu ini membedakan kerang bulu dari jenis kerang lainnya dan memberi mereka penampilan yang unik.</p>
      <p>2.Habitat di Lumpur dan Pasir
        Kerang bulu biasanya hidup di dasar laut yang berlumpur atau berpasir di perairan dangkal, membuat mereka mudah ditemukan di pantai-pantai berlumpur.</p>
        <p>3.Rasa yang Khas
          Daging kerang bulu memiliki tekstur yang lembut dan rasa gurih yang khas, membuatnya digemari dalam berbagai hidangan, seperti tumis kerang, kerang saus tiram, hingga sate kerang.</p>
          <p>4.Pengaruh Terhadap Ekosistem
            Kerang bulu berperan penting dalam menjaga keseimbangan ekosistem laut. Mereka membantu menyaring air laut dan membersihkan partikel-partikel kecil, sehingga membantu menjaga kualitas air di sekitarnya.</p>
        <p><b>"Kerang bulu sering dikonsumsi dalam berbagai masakan dan memiliki manfaat kesehatan yang cukup baik jika diolah dengan benar."</b></p>
