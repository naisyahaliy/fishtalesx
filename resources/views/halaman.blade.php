    <h2 style="font-family: 'Franklin Gothic Medium', 'Arial Narrow', Arial, sans-serif;">Jenis-jenis Bintang Laut</h2><br>
    <h3>1.Bulu Seribu</h3>
    <img src="https://www.mongabay.co.id/wp-content/uploads/2017/08/Bulu-seribu-2.jpg" style="width: 80%; margin-top: 3%;"><br>
    <p style="margin-top: 3%;">Bintang laut mahkota duri atau Bulu seribu adalah spesies bintang laut berlengan sangat banyak yang seluruh tubuhnya ditutupi duri yang sangat banyak pula. Habitat asli dari bintang laut mahkota duri adalah di terumbu karang pada kawasan Indo-Pasifik.</p>
    <h3>Keunikan Bulu Seribu</h3>
    <h4>1.Bintang laut dengan banyak lengan</h4>
    <p style="margin-top: 3%;">Bulu seribu merupakan bintang laut tropis yang berukuran cukup besar. Dicatat oleh laman Animal Diversity, bulu seribu dewasa memiliki total diameter antara 25—35 sentimeter. Beberapa bahkan bisa tumbuh lebih dari 70 sentimeter!Bintang laut bernama ilmiah Acanthaster planci ini punya 8—21 lengan. Sama seperti bintang laut pada umumnya, mereka bisa menumbuhkan lagi lengannya yang terputus walaupun perlu waktu setidaknya 6 bulan untuk proses regenerasinya, menurut laman Critter Facts. Warna bulu seribu juga bervariasi. Ada yang berwarna abu-abu, merah-oranye, sampai keunguan. Makanan dan tempat tinggal bulu seribu diduga memengaruhi pewarnaan tubuhnya.</p>

    <h4 style="margin-top: 3%;">2.Tubuhnya berselimutkan duri tajam beracun!</h4>
    <p style="margin-top: 3%;">Yang unik dari bintang laut satu ini adalah duri-duri tajam yang menyelimuti tubuhnya. Tampilannya yang bak mahkota menginspirasi penamaannya dalam bahasa Inggris, yakni crown of thorns atau mahkota berduri. Menurut Critter Facts, duri bulu seribu bisa tumbuh hingga panjang 5 sentimeter. Duri-duri ini digunakan untuk mempertahankan diri dari predator. Ya, durinya cukup tajam untuk bisa melukai tubuh predator, bahkan bisa menembus pakaian selam! Penyelam yang menyelam di area tempat tinggal bulu seribu selalu disarankan untuk berhati-hati.Racun yang terkandung dalam jaringan duri bulu seribu masih belum diketahui benar. Namun, laman Thoughtco mengungkap kalau duri bulu seribu bisa menyebabkan rasa sakit menyengat, bengkak, mual, sampai muntah-muntah pada manusia.</p>

    <h4 style="margin-top: 3%;">3.Santap banyak karang per hari</h4>
    <p style="margin-top: 3%;">Bulu seribu merupakan koralivor atau hewan pemakan karang. Saat makan, mereka akan menempel pada karang lalu mengeluarkan perutnya. Enzim pencernaan kemudian keluar dari perut untuk mencerna karang, lalu bulu seribu tinggal menyerap nutrisinya. Proses ini bisa memakan waktu sampai beberapa jam. Biarpun begitu, mereka menyantap banyak karang per hari, lho. Dilansir laman Animal Spot, seekor bulu seribu yang tinggal di Great Barrier Reef mengonsumsi 60—155 sentimeter persegi per harinya saat musim dingin. Angkanya meningkat sampai 478 sentimeter persegi saat musim panas. </p>

    <h3>2.Sunflower sea star</h3>
    <img src="https://marinesanctuary.org/wp-content/uploads/2023/08/52732435382_32146f6053_c-700x525.jpg" style="width: 80%; margin-top: 3%;"><br>
    <p style="margin-top: 3%;">Bintang laut bunga matahari ( Pycnopodia helianthoides ) dinamai berdasarkan lengan radialnya yang menyerupai kelopak kuning bunga matahari yang mungkin Anda temukan di taman seseorang. Bintang laut bunga matahari adalah spesies bintang laut terbesar di lautan global, dengan 24 lengan besar dan diameter hampir tiga kaki dari ujung lengan ke ujung lengan saat dewasa. Mereka dapat tumbuh hingga beratnya mencapai 13 pon dan, meskipun ukurannya besar, spesies ini mungkin juga merupakan bintang laut tercepat di dunia. Seperti bintang laut lainnya, bintang laut bunga matahari dapat meregenerasi lengan yang rusak atau hilang, dan pada dasarnya dapat mengkloning dirinya sendiri jika lengan yang terlepas memiliki sebagian cakram pusat hewan yang menempel padanya. 

      Bintang laut bunga matahari adalah makhluk berwarna-warni, dengan kulit yang bisa berwarna ungu, jingga, cokelat, atau kuning. Ia memiliki duri putih di sekujur tubuhnya dan kaki tabung yang menutupi masing-masing dari 24 lengannya untuk membantu bintang laut bergerak di sepanjang dasar laut. Kaki tabung juga membantu bintang laut untuk mencengkeram batu, permukaan lain, dan memangsa saat dibutuhkan.  
      
      Bintang laut bunga matahari memiliki kerangka unik yang tersusun dari potongan-potongan yang terpisah, yang memungkinkan bintang laut menjadi sangat fleksibel dan bergerak cepat. Kebanyakan bintang laut memiliki kerangka tunggal yang tetap. Lambung bintang laut bunga matahari terdapat di bagian ventral atau bagian bawah dekat bagian tengah tubuhnya, yang juga dikenal sebagai permukaan oral . Posisi ini memungkinkan hewan tersebut memperbesar tubuhnya dan membuka mulutnya lebar-lebar untuk menangkap mangsa yang besar.  </p>
    <h5>Bintang laut ini memiliki beberapa ciri-ciri, di antaranya: </h5>
     <p>-Ukurannya besar, dengan diameter hampir tiga kaki dari ujung lengan ke ujung lengan saat dewasa. </p>
      <p>-Memiliki 24 lengan besar. </p>
      <p>-Kulitnya lembut dan berwarna ungu, cokelat, oranye, atau kuning. </p>
      <p>-Karnivora, memakan bulu babi, ikan, kerang, krustasea, remis, teripang, gastropoda, sand dollar, dan terkadang spons dan alga. </p>
      <p>-Predator yang rakus, berayun di atas 15.000 kaki tabungnya saat mencari makanan. </p>
      <p>-Bergerak dengan kecepatan lebih dari 40 inci (1 m) per menit.</p> 
     <h5> Bintang laut bunga matahari saat ini diklasifikasikan sebagai spesies yang sangat terancam punah oleh Persatuan Internasional untuk Konservasi Alam. Hal ini disebabkan oleh beberapa faktor, di antaranya:</h5> 
      <p>-Penyakit yang telah membunuh lebih dari 90% bintang laut bunga matahari. </p>
      <p>-Pengasaman laut.</p> 

      <h3>3.Asterias amurensis</h3>
      <img src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/f3/Asterias_amurensis_53110309.jpg/640px-Asterias_amurensis_53110309.jpg" style="width: 80%; margin-top: 3%;"><br>
      <p style="margin-top: 3%;">Diterjemahkan dari bahasa Inggris-Asterias amurensis, juga dikenal sebagai bintang laut Pasifik Utara dan bintang laut biasa Jepang, adalah bintang laut yang ditemukan di laut dangkal dan muara, asli pantai Cina utara, Korea, Rusia timur jauh, Jepang, Alaska, Kepulauan Aleutian dan British Columbia di Kanada.
        Asterias amurensis (bintang laut Pasifik utara) dapat tumbuh hingga berdiameter 50 cm. Warnanya kuning dengan pigmentasi merah dan ungu pada kelima lengannya, dan cakram kecil di bagian tengah. Ciri khasnya adalah ujungnya yang terbalik yang menjadi kunci identifikasinya jika dibandingkan dengan bintang laut sejenis. Bagian bawahnya sepenuhnya berwarna kuning dan lengannya ditutupi duri-duri kecil bergerigi secara tidak merata (CSIRO, 2004). Duri-duri ini melapisi alur tempat kaki tabung berada, dan menyatu di bagian mulut dalam bentuk seperti kipas
      </p>
      <h5>Bintang laut ini memiliki beberapa karakteristik unik yang membuatnya terkenal:</h5>
      <p>1.Sangat Invasif : Asterias amurensis dikenal sebagai salah satu spesies laut yang paling invasif. Spesies ini telah menyebar ke wilayah lain, seperti Australia, dan mengancam ekosistem laut asli dengan memangsa kerang dan kehidupan laut lainnya, sehingga mengganggu keseimbangan.</p>
      <p>2.Reproduksi Cepat : Spesies ini bereproduksi secara produktif. Seekor betina dapat melepaskan jutaan telur, sehingga meningkatkan potensi ledakan populasi, yang berkontribusi pada sifat invasifnya.</p>
      <p>3.Pola Makan Luas : Asterias amurensis memiliki pola makan yang sangat bervariasi, mengonsumsi kerang, kepiting, teritip, bulu babi, dan invertebrata lainnya. Perilaku makan oportunistik ini memungkinkannya untuk tumbuh subur di lingkungan yang berbeda.</p>
      <p>4.Kemampuan Regenerasi : Seperti banyak bintang laut lainnya, ia dapat meregenerasi lengan yang hilang, sehingga ia dapat bertahan hidup dari cedera atau serangan predator. Kemampuan regeneratif ini juga membantu populasinya pulih dengan cepat dari upaya pengendalian populasi.</p>
      
      <h3>4.Culcita novaeguineae</h3>
      <img src="https://shop.dejongmarinelife.nl/media/catalog/product/cache/256814665417d31a06451205e2bc322a/S/C/SC-NOV-042351-group.jpg" style="width: 80%; margin-top: 3%;"><br>
      <p style="margin-top: 3%;">Tidak seperti bintang laut pada umumnya yang memiliki lengan panjang dan menonjol, bintang laut bantal memiliki tubuh yang lebih membulat dan mengembang, sehingga tampak seperti bentuk bantal atau bentuk seperti segi lima. Lengannya pendek dan tidak terlalu menonjol.
        Warnanya bisa bervariasi, biasanya muncul dalam nuansa coklat, hijau, kuning, atau bahkan merah muda dengan bintik-bintik. Tumbuhan ini dapat tumbuh hingga mencapai diameter 30 cm (12 inci), sehingga relatif besar dibandingkan dengan spesies lainnya.</p>
        <p style="margin-top: 3%;">Lengan dari Culcita novaeguineae sangat pendek dan hampir tidak terlihat, yang membuatnya berbeda dari sebagian besar bintang laut yang memiliki lengan. Beberapa hewan kecil, seperti udang atau kepiting, terkadang hidup bersama Culcita novaeguineae. Mereka tinggal di permukaan atau di antara tubuhnya untuk perlindungan, membentuk hubungan simbiotik. Hal ini tidak lazim ditemukan pada banyak spesis lain.</p>

        <h3>5.Brisingida</h3>
      <img src="https://darilaut.id/wp-content/uploads/2020/11/Brisingida-709x375.jpg" style="width: 80%; margin-top: 3%;"><br>
      <p style="margin-top: 3%;">Brisingida adalah ordo bintang laut (kelas Asteroidea) yang berisi bintang laut yang hidup di laut dalam.Brisingida tersebar di sejumlah lokasi laut dalam, khususnya di Karibia dan Selandia Baru.Spesies anggotanya ditemukan dalam berbagai ukuran, terutama di Samudera Pasifik bagian timur pada kedalaman 1.820-2.418 meter dibawah permukaan laut</p>
        <p style="margin-top: 3%;">Bintang laut ini memiliki antara 6 dan 18 lengan panjang dan lemah yang mereka gunakan untuk memakan suspensi. Karakteristik lain termasuk satu rangkaian marginal, cincin piringan yang menyatu, tidak adanya pelat aktinal, kolom ambulakral seperti spul, pelat abaktinal yang tereduksi, dan pediselaria yang bersilangan. Mereka 40 kali ukuran radius cakram dan memiliki 7-20 lengan berduri yang fleksibel.</p>

