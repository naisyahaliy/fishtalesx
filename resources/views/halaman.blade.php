        <h3 style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">Makanan Bintang Laut dan Cara Unik Memakan Mangsanya</h3>
        <p>Bintang laut (Asteroidea) adalah hewan karnivora yang memakan berbagai jenis makanan, di antaranya:
          Moluska, seperti remis, kerang, dan tiram,
          Karang,
          Spons,
          Sand dollar,
          Rumput laut,
          Ikan kecil.

          Bintang laut memiliki cara yang unik untuk memakan mangsanya, yaitu: 
          Menggunakan kaki yang memiliki cangkir penghisap untuk mencengkeram mangsanya,
          Kemudian Membalikkan tubuhnya untuk makan,
          Mengeluarkan perutnya dari mulut untuk mencerna makanan,
          Lalu Menyerap makanan di luar tubuhnya ,
          Dan Memasukkan kembali perutnya ke dalam tubuh setelah selesai makan.
          Bintang laut juga berperan penting dalam menjaga kebersihan pantai karena memakan bangkai dan cangkang-cangkang mollusca yang mengotori pantai. 
