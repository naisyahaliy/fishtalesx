      <div class="col-md-6 info-text">
        <h3 style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif">Apasih Bintang Laut Itu?</h3>
        <p>Bintang Laut adalah satu makhluk laut yang paling menarik dan mudah dikenali karena bentuk tubuhnya yang menyerupai bintang. Hewan ini hidup di berbagai jenis perairan laut di seluruh dunia, dari pantai yang dangkal hingga kedalaman laut yang gelap. Meskipun bentuknya terlihat sederhana, bintang laut memiliki banyak keunikan dan fakta menarik yang jarang diketahui.

            Bintang laut termasuk dalam kelompok hewan laut bernama Echinodermata, bersama dengan bulu babi dan teripang. Ada lebih dari 2.000 spesies bintang laut yang tersebar di berbagai belahan dunia. Kebanyakkan bintang laut memiliki lima lengan, tetapi ada juga spesies yang memiliki lebih banyak, bahkan hingga 40 lengan.
            
            Salah satu fakta paling menarik tentang bintang laut adalah kemampuannya untuk meregenerasi tubuh. Jika salah satu lengannya putus, bintang laut dapat menumbuhkan lengan baru. Bahkan, dalam beberapa kasus, bagian tubuh bintang laut yang putus pun dapat tumbuh menjadi individu baru.
            
            Meskipun tidak memiliki otak, bintang laut memiliki sistem saraf yang cukup kompleks. Sistem saraf ini memungkinkan mereka untuk merasakan lingkungan sekitar dan merespons rangsangan.</p>
