        <h3  style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">Keunikan Bintang Laut</h3>
        <h5  style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">~Bintang laut memiliki perisai pelindung</h5>
        <p>
           Fakta unik bintang laut adalah perisai berupa cangkang kaku yang memiliki tekstur kasar, bahkan berduri, tergantung spesiesnya. Bintang laut memiliki lapisan kalsium karbonat dengan duri-duri kecil hal ini membuat ikan, burung, dan bahkan berang-berang laut terhalang untuk menyerang mereka.
          </p><br>
          <h5  style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">~Bintang laut bisa merasakan sakit</h5>
          <p>Meski tidak memiliki otak, jantung, atau pembuluh darah, bintang laut masih bisa merasakan sakit berkat sistem sarafnya yang sangat berkembang. Bintang laut memiliki insang di bagian luar tubuhnya yang memungkinkan mereka menghirup oksigen dari air. Jadi jangan pernah mengambil atau mengeluarkannya dari air, itu dapat menyebabkannya mati.
          </p><br>
          <h5  style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">~Bintang laut bisa meregenerasi bagian tubuh yang rusak</h5>
          <p>Bintang laut terkenal tidak hanya karena bentuknya yang tak biasa tetapi kemampuannya untuk menumbuhkan kembali anggota tubuh yang hilang. Menariknya, setiap segmen bintang laut akan beregenerasi menjadi bintang laut dewasa baru jika bagian tengah hewan tersebut dipotong secara radial. 
          </p><br>
          <h5  style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">~Bintang laut dapat dimakan</h5>
          <p>Di beberapa kota, termasuk Beijing, China pedagang kaki lima menjual sushi yang terbuat dari bintang laut. Beberapa orang menyukai bintang laut tetapi yang lain tidak. Paus Fransiskus Tiba di Timor Leste Artikel Kompas.id Ada yang berpikir bahwa rasanya seperti kepiting sungai atau bulu babi tetapi ada pula yang mengklaim rasanya tak ada sama sekali.
          </p><br>
          <h5  style="font-family: 'Trebuchet MS', 'Lucida Sans Unicode', 'Lucida Grande', 'Lucida Sans', Arial, sans-serif;">~Bintang laut bertahan hidup 28 jam di luar air</h5>
          <p>Bintang laut hanya dapat menghabiskan waktu singkat di luar air. Oleh karena itu, setiap penampakan mereka di pantai atau batu besar saat air surut cenderung bersifat sementara. Akan tetapi, beberapa jenis bintang laut yang lebih besar dapat tetap sehat dan hidup hingga 28 jam di luar air.
          </p><br>
