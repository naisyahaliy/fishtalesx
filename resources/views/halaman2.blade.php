            <h2>Jenis-jenis ikan</h2>
            <h4 style="margin-top: 3%">1.Ikan Buntal (Tetraodontidae)</h4>
            <img src="https://www.tintahijau.com/wp-content/uploads/2024/04/ikan-unik-pixabay-ArtificialArtist.jpeg" class="card-img-top" alt="Gambar Perairan" style="max-height: 500px; object-fit: cover;">
            <div class="card-body">
                <p class="card-text">
                    Ikan buntal, yang disebut ikan fugu, memiliki kemampuan untuk mengembang saat melindungi diri.

                    Walaupun memiliki bentuk yang unik dan lucu, ikan ini mengandung racun yang sangat kuat, yaitu tetrodoksin.
                    
                    
                    Racun ini ditemukan pada kulit dan organ dalam ikan buntal , termasuk usus, hati dan ovarium. Tertodoksin dapat menyebabkan diare, sakit perut, pegal, hingga kesulitan bergerak atau berbicara.
                </p>
            <h4 style="margin-top: 3%">2. Ikan Batu (Stonfish/ Synanceia verrucosa)</h4>
            <img src="https://encrypted-tbn3.gstatic.com/images?q=tbn:ANd9GcQkJg5x1oVgt51kBPPtyiRU9VizsaXSJoxtjW5xE511mhc4ovKV" class="card-img-top" alt="Gambar Perairan" style="max-height: 500px; object-fit: cover;">
            <div class="card-body">
                <p class="card-text">
                    Ikan batu, Synanceia verrucosa, juga disebut sebagai batu karang atau dornorn, adalah ikan karnivora dengan jarum beracun yang tinggal di dasar terumbu karang, berkamuflase sebagai batu. Ikan ini memiliki panjang sekitar 30-40 cm.
                    Ikan batu adalah ikan paling beracun di dunia. Ia memiliki kemampuan untuk memperbesar tubuhnya menjadi bola berduri apabila terancam.
                    Sengatannya dapat menyebabkan rasa sakit, pembengkakan, kematian jaringan, kelumpuhan sementara, hingga kematian.
                </p>
                <h4 style="margin-top: 3%">3.Queen Angelfish</h4>
            <img src="https://upload.wikimedia.org/wikipedia/commons/9/9f/Queen_Angelfish.jpg" class="card-img-top" alt="Gambar Perairan" style="max-height: 500px; object-fit: cover;">
            <div class="card-body">
                <h5 class="card-text">
                    Queen Angelfish merupakan ikan yang banyak ditemukan di terumbu karang Atlantik Barat pada kedalaman 3,5-230 kaki dibawah permukaan.
                </h5>
                <p>Spesies ini memiliki tubuh datar yang besar berwarna biru cerah serta memiliki sisik dan ekor berwarna kuning cerah. Ikan ini juga memiliki ciri khas seperti tambalan biru laut yang terletak di atas kepala bagian tengah dengan bitnik-bintik biru muda yang menyerupai mahkota. Tubuh ratu angelfish (Holacanthus ciliaris) sangat padat dan kepalanya tumpul dan bulat. Ia memiliki satu sirip punggung panjang di sepanjang bagian atasnya, sirip punggung dan dubur, dan kisaran antara 9–15 duri dan jari lunak. Rata-rata, queen angelfish tumbuh sekitar 12-17,8 inci dan berat hingga 3,5 pon. Mereka memiliki mulut kecil dengan gigi ramping seperti sikat di pita sempit yang dapat menonjol ke luar. Meskipun mereka dominan berwarna biru dan kuning, populasi daerah yang berbeda terkadang memiliki variasi warna yang berbeda, seperti warna emas sesekali, dan bercak hitam dan oranye. Queen angelfish adalah dari ordo Perciformes, keluarga Pomacanthidae, dan genus Holacanthus.</p>
                <h5>Berikut adalah beberapa fakta unik tentang ikan queen angelfish: </h5>
                <p>1.Warna</p>
                <p>Ikan queen angelfish umumnya berwarna biru dan kuning, tetapi ada variasi warna lainnya seperti emas, hitam, dan oranye.</p>
                <p>2.Mahkota</p>
                <p>Ikan queen angelfish memiliki bintik biru tua di dahinya yang dikelilingi oleh bintik-bintik biru muda dan garis biru terang.</p>
                <p>3.Bentuk tubuh</p>
                <p>Ikan queen angelfish memiliki tubuh yang padat, kepala yang bulat dan tumpul, serta sirip punggung yang panjang</p>
                <p>4.Makanan</p>
                <p>Ikan queen angelfish adalah omnivora yang memakan spons, alga, bryozoa, ubur-ubur, karang, plankton, dan tunicates.</p>
                <h4 style="margin-top: 3%">4.Ikan brachionichthys hirsutus</h4>
            <img src="https://cdn.idntimes.com/content-images/community/2023/10/800px-brachionichthys-hirsutus-rls-5be77dd23a73aaf9d3374da589f75fbe-06bfc205c029764cb949b5d464caa401_600x400.jpg" class="card-img-top" alt="Gambar Perairan" style="max-height: 500px; object-fit: cover;">
            <div class="card-body">
                <p class="card-text">
                    Ikan bertangan bintik atau dalam bahasa Inggris dikenal sebagai Spotted Handfish (lat. Brachionichthys hirsutus) adalah ikan laut yang sangat unik karena ia menggunakan siripnya seperti tangan untuk "berjalan" di dasar laut. Ikan ini termasuk dalam famili Brachionichthyidae dan merupakan salah satu spesies ikan yang paling langka di dunia.
                    <h5>Ikan ini sering di sebut ikan mager loh? kenapa yaa?</h5>
                    <h4>Alasan Mengapa Disebut Ikan Mager:</h4>
                    <p>1. Lebih Sering "Berjalan" daripada Berenang: Alih-alih berenang aktif seperti ikan pada umumnya, ikan bertangan bintik menggunakan sirip pektoralnya yang mirip tangan untuk berjalan perlahan di dasar laut. Gerakan ini tampak santai dan lamban, sehingga memberikan kesan ikan ini kurang suka bergerak.</p>
                    <p>2.Jarang Berenang: Ikan bertangan bintik cenderung tidak berenang jauh atau cepat. Mereka lebih suka berdiam di dasar laut, menunggu mangsa lewat, atau bergerak sedikit untuk mencari makanan. Ini adalah ciri predator penyergap yang mengandalkan kamuflase dan ketenangan.
                    <p>3. Habitat di Dasar Laut: Ikan ini menghabiskan sebagian besar waktunya di dasar laut yang berpasir atau berlumpur, di mana mereka bisa beristirahat atau berjalan perlahan di sekitarnya. Mereka tidak aktif menjelajahi kolom air seperti ikan-ikan lainnya.</p>
                    <h5>Secara keseluruhan, kebiasaan ikan ini yang tidak banyak bergerak dan lebih suka "berjalan" di dasar laut membuatnya terlihat seperti ikan yang "malas gerak" atau mager, meskipun perilaku ini adalah adaptasi yang efisien untuk bertahan hidup di habitatnya.</h5>
                    </p>
                </p>
                <h4 style="margin-top: 3%">5.Ikan Piranha</h4>
                <img src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcTZB1eUL29MOmrRZ-FFilKag-hUlAvMjVfjgg&s" class="card-img-top" alt="Gambar Perairan" style="max-height: 500px; object-fit: cover;">
                <div class="card-body">
                    <p class="card-text">
                        Bagi Grameds yang pernah menonton film Piranha, pasti sudah tidak asing lagi dengan  ikan yang satu ini. Ikan ini tampak menyeramkan dengan gigi yang runcing dan siap menerkam jika mereka datang dengan gerombolannya. Pesta pantai yang dinikmati para turis langsung berubah menjadi teror yang menyeramkan.
                        <p>Ikan piranha adalah jenis ikan air tawar yang terkenal dengan reputasinya sebagai predator agresif. Mereka berasal dari sungai-sungai di Amerika Selatan, terutama di Amazon, dan dikenal karena giginya yang tajam serta perilaku makan daging. Meskipun sering dianggap berbahaya bagi manusia, piranha sebenarnya lebih sering menjadi pemakan bangkai dan jarang menyerang manusia kecuali dalam kondisi tertentu.</p>
                        <h5>Mitos dan Fakta Tentang Ikan Piranha</h5>
                        <p>Mitos: Piranha sering digambarkan dalam budaya populer sebagai pembunuh yang berbahaya bagi manusia, seperti yang ditampilkan dalam film-film dan cerita horor.</p>
                        <p>Fakta: Pada kenyataannya, serangan piranha terhadap manusia sangat jarang terjadi. Kebanyakan piranha lebih tertarik pada ikan lain atau bangkai hewan daripada menyerang manusia.</p>
                        <h5>Fakta Menarik tentang Ikan Piranha</h5>
                        <p>-Piranha telah ada selama sekitar 1,8 juta tahun, menurut bukti fosil.</p>
                        <p>-Gigi piranha terus tumbuh sepanjang hidup mereka dan akan digantikan jika ada yang patah.</p>
                        <p>-Mereka memiliki daya penciuman yang sangat tajam, dan mereka dapat mendeteksi darah di air dari jarak yang cukup jauh.</p>
                    </p>
                    <h4 style="margin-top: 3%">6.Ikan Mola</h4>
                    <img src="https://encrypted-tbn3.gstatic.com/images?q=tbn:ANd9GcS00etKQ2iNmdOtAHwfUfS8LOrE3qUv3-kN8XbdWQBSdLUcah1X" class="card-img-top" alt="Gambar Perairan" style="max-height: 500px; object-fit: cover;">
                    <div class="card-body">
                        <p class="card-text">
                            Apa kalian tahu ikan mola-mola, atau dalam bahasa inggris sunfish (ikan matahari)? Itu lho ikan yang sangat sering terdampar di pinggir pantai, gak tau? Baiklah akan aku jelaskan ya. 
                            <p>Ikan mola-mola atau Ocean Sunfish merupakan salah satu ikan, yang masih berkerabat dekat dengan ikan buntal dan ikan triggerfish. Ikan mola-mola terdapat diseluruh perairan di dunia kecuali kutub Utara dan Selatan. Nama ikan “mola-mola” bukan hanya sebatas nama, melainkan ada artinya. Nama “mola” diambil dari bahasa latin yang artinya batugiling (millstone) yang merujuk pada tubuh ikan mola-mola berbentuk seperti batugilling yang bulat pipih, berat dan berwarna abu-abu (kok seperti peluru ya bentuknya). Tapi bukan itu saja lho, masih ada beberapa fakta unik ikan gembul kita satu ini. Penasaran, yuk kita baca penjelasan dibawah ini.</p>
                            <p>1. Ukurannya yang “Wow banget” </p>
                            <p>Ikan mola-mola atau sunfish diketahui sebagai ikan bertulang belakang terberat dan terbesar diseluruh dunia. Dengan ukuran tubuh sekitar 10 kaki atau sekitar 305cm/ 3,05m (Gedhe banget!). Dan berat rata-rata sekitar 247 sampai 1000 kg bahkan ikan mola-mola terbesar memiliki berat hingga 2,5 ton (berarti lebih berat dari mobilku dong hehe....).</p>
                            <p>2. Bentuknya yang unik banget. </p>
                            <p>Ikan mola-mola memiliki  kulit yang unik, dimana jika dilihat dari jauh kulitnya seperti licin dan berlendir, tapi jika kita sentuh maka akan terasa berat dan keras seperti batu. Ikan ini tidak seperti ikan lainnya yang mempunyai ekor, ia hanya memiliki  semacam perpaduan sirip dorsal dan sirip anal yang disebut clavius. Sehingga dalam bahasa Jerman sunfish disebut Schwimmender Kopf yang berarti “kepala berenang”. Sementara di Polandia sunfish disebut Samoglow, yang berarti “kepala” saja. Karena tidak punya ekor, membuat sunfish menjadi lambat dalam berenang yang menyebabkan ia sering terbawa arus air yg membuat ia sering terdampar. (kok sedih ya)</p>
                            <p>3. Sangat suka berjemur. </p>
                            <p>Ikan Sunfish tidak sembarangan disebut sunfish (ikan matahari), melainkan nama tersebut diambil dari kebiasaan sunfish yang suka berjemur. Ya, Berjemur. Sunfish merupakan hewan yang sangat mudah terkena serangan parasit. Dalam suatu kasus pernah dijumpai 40 genera parasit yang menempel pada kulit ikan sunfish (kasihan). Untuk itu, sunfish menghilangkan parasitnya dengan cara Berjemur (ternyata ikan juga bisa menjaga kebersihan ya). Jadi sunfish akan membaringkan tubuhnya satu mengarah keatas dan satu lagi kebawah dan mebalikkan tubuhnya beberapa menit sekali sehingga seluruh tubuhnya terkena cahaya matahari. Terkadang sunfish suka mengepakkan sirip punggung mereka saat berjemur, menandakan mereka senang (lucu banget!).</p>
                        </p>
