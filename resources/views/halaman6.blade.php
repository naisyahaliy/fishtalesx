            <div class="card-body">
                <p class="card-text">Mesonychoteuthis hamiltoni atau biasa disebut cumi-cumi kolosal, mempunyai mata sebesar piring makan. Spesies ini diketahui cumi-cumi terbesar yang pertama ditemukan. Pertama kali difoto setelah hewan ini terjebak di sebuah jaring perahu pencari ika</p>

