            <h4>3.Udang Vaname/Whiteleg</h4>
            <img src="https://www.blibli.com/friends-backend/wp-content/uploads/2024/04/B130364-6-Udang-Vaname-Whiteleg-1024x538.jpg" style="width: 50%; margin-left:20%;">
            <p style="margin-top: 3%;">Udang Vaname atau whiteleg shrimp (Litopenaeus vannamei) adalah salah satu jenis udang yang sangat populer dalam budidaya akuakultur di seluruh dunia, terutama di Asia dan Amerika Selatan. Udang ini dikenal karena pertumbuhannya yang cepat, toleransi terhadap kondisi lingkungan yang beragam, dan tingkat produksi yang tinggi, sehingga menjadi pilihan utama bagi para peternak udang.</p>
          
            <h4>4.Udang Spot (Pandalus Platyceros)</h4>
            <img src="https://tambakmilenial.com/wp-content/uploads/2022/10/jenis-udang-laut-yang-bisa-dimakan-1024x574.webp" style="width: 50%; margin-left:20%;">
            <p style="margin-top: 3%;">Udang spot disebut juga dengan nama lobster Alaska atau udang tutul. Hal ini karena pada tubuh udang terdapat  titik-titik yang bentuk dan warnanya mirip dengan yang ada pada lobster. 

              Sedangkan warna seluruh badannya adalah campuran antara warna kemerahan dan oranye. Udang Spot banyak ditemukan di pesisir Samudra Pasifik Utara, dari San Diego sampai ke Alaska.
              
              Ukuran dari udang spot cukup besar bisa mencapai 30 cm. Daging udang spot terasa manis saat dimakan. Anda dapat mengolahnya dengan cara apapun. Namun cara terbaik untuk menikmati udang ini adalah dengan dibuat menjadi sushi. Selain itu sebaiknya kepala udang spot digunakan untuk membuat air kaldu daripada dibuang.</p>

              <h4>5.Udang Sikat (Thenus Orientalis)</h4>
            <img src="https://tambakmilenial.com/wp-content/uploads/2022/10/jenis-udang-laut-indonesia-1024x682.webp" style="width: 50%; margin-left:20%; ">
            <p style="margin-top: 3%;">Selanjutnya adalah jenis udang laut dengan bentuk tubuh yang unik, sebutannya adalah udang sikat, udang kipas, atau udang pasir. Bentuknya sekilas mirip dengan lobster, tetapi udang kipas berukuran lebih kecil dengan kulit yang lunak. Di luar negeri udang ini disebut dengan slipper lobster. Banyak ditemukan di sepanjang pesisir wilayah perairan tropis. Seperti Laut Mediterania, laut merah, lepas pantai Afrika Timur dan Australia Bagian yang bisa dimakan hanya daging ekornya saja. Anda dapat mengolahnya dengan saus padang, balado, atau asam manis.</p>
